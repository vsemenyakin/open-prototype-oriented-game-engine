#include "PLCacheManager.h"

///////////////////////////////////////////////////////////////////////////////
PLCacheManager::~PLCacheManager()
{
	while (cachesWrappers.size() != 0)
	{
		PLCacheWrapper *theWrapper = cachesWrappers.pop();
		delete theWrapper;
	}
}

///////////////////////////////////////////////////////////////////////////////
PLCacheManager::PLCacheWrapper *PLCacheManager::newCacheWrapper()
{
	PLCacheWrapper *theWrapper = new PLCacheWrapper();
	cachesWrappers.push(theWrapper);

	return theWrapper;
}

